      this.addressList = list.length > 0 ? list : this.addressList
      console.log(this.addressList)
    }
  },
  async created () {
    // 一进入页面, 发送请求, 获取用户收货地址列表
    await this.setAddressList() // 调用'获取用户收货地址列表'方法
  }
}
